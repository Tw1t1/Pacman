    public static int[][] getGrid(){
      switch(level) {
        case 1: return grid1;
        case 2: return grid2;
        default: return grid0;
      }
    }

    public boolean nextLevel(){
      level += 1; 
      if(level > 2) {
        return false;
      }
      return true;
    }

    public static int getCurrentGrid(){
      return level;
    }

    public boolean getLastGrid() {
      return lastGrid;
    }

    public boolean collision(float x, float y, int width, int height){
        return true;
    }

    public boolean isCoinLocation(int row, int col) {
      switch(level) {
        case 1: return (!(row == 13 && col == 12) && !(4 < row && row < 8 && 10 < col && col < 14)); //grid1
        case 2: return (true); // grid2
        default: return (true); // grid3
      }
    }

    public float getGhostStartX(){
      switch(level) {
        case 1: return (1 * BLOCK_WIDTH); // grid1
        case 2: return (2 * BLOCK_WIDTH); // grid2
        default: return (10 * BLOCK_WIDTH); // grid3
      }
    }


    public float getGhostStartY(){
      switch(level) {
        case 1: return (1 * BLOCK_HEIGHT + InfoBar.HEIGHT); // grid1
        case 2: return (2 * BLOCK_HEIGHT + InfoBar.HEIGHT); // grid2
        default: return (5 * BLOCK_HEIGHT + InfoBar.HEIGHT); // grid0
      }
    }


    public void render(Graphics g) {
        int[][] grid = getGrid();
        int correctionY, correctionX;
        for (int row = 0; row < grid.length; row++) {
            for (int col = 0; col < grid[row].length; col++) {
                if (grid[row][col] == 1) {
                    correctionX = (col == grid[row].length - 1)? 1 : 0;
                    correctionY = (row == grid.length - 1)? 1 : 0;
                    g.setColor(Color.BLACK);
                    g.fillRect(col * (int)(BLOCK_WIDTH), row * (int)(BLOCK_HEIGHT) + InfoBar.HEIGHT, (int)(BLOCK_WIDTH), (int)(BLOCK_HEIGHT));
                    g.setColor(Color.BLUE);
                    g.drawRect(col * (int)(BLOCK_WIDTH), row * (int)(BLOCK_HEIGHT) + InfoBar.HEIGHT, (int)(BLOCK_WIDTH) - correctionX, (int)(BLOCK_HEIGHT) - correctionY);
                    if(grid == grid1) {
                      g.setColor(Color.PINK);
                      g.drawRect(col * (int)(BLOCK_WIDTH), row * (int)(BLOCK_HEIGHT) + InfoBar.HEIGHT, (int)(BLOCK_WIDTH) - correctionX, (int)(BLOCK_HEIGHT) - correctionY);
                    }else if(grid == grid2) {
                      g.setColor(new Color(105,0,242));
                      g.drawRect(col * (int)(BLOCK_WIDTH), row * (int)(BLOCK_HEIGHT) + InfoBar.HEIGHT, (int)(BLOCK_WIDTH) - correctionX, (int)(BLOCK_HEIGHT) - correctionY);
                    }
                }
            }
        }
    }
